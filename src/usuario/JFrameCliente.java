            erro += "Bairro inválido.\n";
        
        if ( cidade.isEmpty() )
            erro += "Cidade inválida.\n";
        
        if ( !estado.matches("\\w{2}$") )
            erro += "Estado inválido.\n";
        
        if ( !cep.matches("[0-9]{5}[0-9]{3}$") )
            erro += "CEP inválido.\n";
        
        if ( !telefone.matches("^\\([1-9]{2}\\) [2-9][0-9]{3,4}\\-[0-9]{4}$") )
            erro += "Telefone inválido.\n";
        
        if ( !email.matches("\\w+@\\w+[.]\\w{3}[.]\\w{2}|\\w+@\\w+[.]\\w{2,3}") )
            erro += "E-mail inválido.";
        
        // Janela contendo os erros encontrados na validação do formulário
        if ( !erro.isEmpty() ) {
            JOptionPane.showMessageDialog(this, erro + ' ' + nome);
        
        // Caso contrário, tenta inserir os dados no banco
        } else {
            Cliente cliente = new Cliente(codigo, nome, endereco, bairro, cidade, estado, cep, telefone, email, data);
            
            try {
                Banco.incluirCliente(cliente);
                JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso.");
                
                // Limpa os campos de inclusão
                jTextFieldNome.setText("");
                jTextFieldEndereco.setText("");
                jTextFieldBairro.setText("");
                jTextFieldCidade.setText("");
                jTextFieldEstado.setText("");
                jTextFieldCEP.setText("");
                jTextFieldTelefone.setText("");
                jTextFieldEmail.setText("");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
