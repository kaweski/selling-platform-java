    /**
     * Método que cadastra um novo vendedor
     * @author Natasha Kaweski
     * @since 12/11/16
     * @version 1.0
     * @param evt 
     */
    private void jButtonIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirActionPerformed
       // Declaração das variáveis
        int codigo = 0;
        String nome = jTextFieldIncluir.getText().toUpperCase();
        String erro = "";
        Date dataCadVendedor = new Date(System.currentTimeMillis());
        
        // Validação dos dados do Cliente
        if ( !nome.matches("[A-Z ÇÃÕÚÁÉ]{3,40}") )
            erro = "Nome inválido.\n";
        
        // Janela contendo os erros encontrados na validação do formulário
        if ( !erro.isEmpty() ) {
            JOptionPane.showMessageDialog(this, erro + ' ' + nome);
        
        // Caso contrário, tenta inserir os dados no banco
        } else {
            Vendedor vendedor = new Vendedor(codigo, nome, dataCadVendedor);
            
            try {
                Banco.incluirVendedor(vendedor);
                JOptionPane.showMessageDialog(this, "Vendedor cadastrado com sucesso.");
                LtpUtil.loadFormatJTable(jScrollPanePesquisar, banco.Banco.listaTodosVendedores(),false);
                
                // Limpa o campo de nome
                jTextFieldIncluir.setText("");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            } catch (LtpUtilException ex) {
                Logger.getLogger(JFrameVendedores.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonIncluirActionPerformed
    
