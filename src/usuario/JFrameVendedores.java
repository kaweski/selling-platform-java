    /**
     * Método que altera um vendedor já cadastrado
     * @author Natasha Kaweski
     * @since 12/11/16
     * @version 1.0
     * @param evt 
     */
    private void jButtonAlterarVendedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarVendedorActionPerformed
        // Captura o que foi digitado no campo de texto de código
        String codigo = jTextFieldBuscarPraAlterar.getText();
        
        // Validação do campo do código
        if ( codigo.isEmpty() || !codigo.matches("[0-9]{1,5}") ) {
            JOptionPane.showMessageDialog(this, "O código deve conter apenas números.");
            return;
        } 
        
        // Checkpoint!
        try {
            ResultSet resposta = banco.Banco.pesquisarVendedorCodigo(Integer.parseInt(codigo));
            
            if ( resposta.next() ) {
        
                // Declaração das variáveis
                String nome = jTextFieldNomeAlterar.getText().toUpperCase();
                String erro = "";
                Date data = new Date(System.currentTimeMillis());

                // Validação dos dados do Vendedor
                if ( !nome.matches("[A-Z ÇÃÕÚÁÉ]{3,40}") )
                    erro = "Nome inválido.\n";

                // Janela contendo os erros encontrados na validação do formulário
                if ( !erro.isEmpty() ) {
                    JOptionPane.showMessageDialog(this, erro + ' ' + nome);

                // Caso contrário, tenta inserir os dados no banco
                } else {
                    Vendedor vendedor = new Vendedor(Integer.parseInt(codigo), nome, data);

                    try {
                        Banco.alterarVendedor(vendedor);
                        JOptionPane.showMessageDialog(this, "Vendedor alterado com sucesso.");
                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(this, e.getMessage());
                    }
                }
                
            } else {
                JOptionPane.showMessageDialog(this, "Não existe cliente para o código digitado.");
            }
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButtonAlterarVendedorActionPerformed
    
    /**
     * Método que exclui um vendedor do banco
     * @author Natasha Kaweski
     * @since 12/11/16
     * @version 1.0
     * @param evt 
     */
    private void jButtonExcluirVendedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirVendedorActionPerformed
        // Pega o cliente com o código informado
        String codigo = jTextFieldBuscarPraAlterar.getText();
        
        // Validação
        if ( codigo.isEmpty() || !codigo.matches("[0-9]{1,5}") ) {
            JOptionPane.showMessageDialog(this, "O código deve conter apenas números.");
            return;
        }
        
        int reply = JOptionPane.showConfirmDialog(null, "Confirmar exclusão?", "Deletar vendedor", JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION) {
            try {
                banco.Banco.excluirVendedor(Integer.parseInt(codigo));
                JOptionPane.showMessageDialog(this, "Vendedor excluído.");
                
                jTextFieldBuscarPraAlterar.setText("");
                jTextFieldNomeAlterar.setText("");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonExcluirVendedorActionPerformed

